pub const fn num_value_features<const S: usize>() -> usize {
    num_square_symmetries::<S>() * 5 + (S + 1) * 3 + 6 * 1 + 3 * 3 + 1 * 6
}

pub const fn num_policy_features<const S: usize>() -> usize {
    num_square_symmetries::<S>() * 3
        + (S * 3) * 2
        + (S - 3) * 4
        + 6 * 3
        + 4 * 2
        + 3 * 16
        + 2 * 3
        + 1 * 13
}

